class SortingSolver:
    """
    Use python built-in sorting function
    for sorting algorithms for simplicity.
    """
    @staticmethod
    def bubble_sort(arr):
        return list(sorted(arr))


    @staticmethod
    def heapsort(arr):
        return list(sorted(arr))


    @staticmethod
    def insertion_sort(arr):
        return list(sorted(arr))


    @staticmethod
    def quicksort(arr):
        return list(sorted(arr))
