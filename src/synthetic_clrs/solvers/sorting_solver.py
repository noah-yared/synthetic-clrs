class SortingSolver:
    """
    Use python built-in sorting function
    for sorting algorithms for simplicity.
    """
    @staticmethod
    def bubble_sort(array, **_):
        return list(sorted(array))


    @staticmethod
    def heapsort(array, **_):
        return list(sorted(array))


    @staticmethod
    def insertion_sort(array, **_):
        return list(sorted(array))


    @staticmethod
    def quicksort(array, **_):
        return list(sorted(array))
